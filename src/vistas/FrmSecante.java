    private boolean validarCampos() {

        if (this.txtFuncion.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese la funcion", "Error", JOptionPane.ERROR_MESSAGE);
            this.txtFuncion.requestFocus();
        } else if (this.aproximacionXo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese el punto inicial del intervalo", "Error", JOptionPane.ERROR_MESSAGE);
            this.aproximacionXo.requestFocus();
        } else if (this.aproximacionXi.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese el punto final del intervalo", "Error", JOptionPane.ERROR_MESSAGE);
            this.aproximacionXi.requestFocus();
        } else if (this.txtError.getText().isEmpty() && this.txtIteracion.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese el error o el numero de iteraciones", "Error", JOptionPane.ERROR_MESSAGE);
            this.txtError.requestFocus();
        } else {
            return true;
        }
        return false;
    }

    private void llenarRespuesta() {

        int row = modelo.getRowCount() - 1;
        this.lblRaiz.setText(modelo.getValueAt(row, 1).toString());
        this.lblError.setText(modelo.getValueAt(row, 3).toString());
        this.lblError2.setText(modelo.getValueAt(row, 4).toString());
        this.lblIteraciones.setText(modelo.getValueAt(row, 0).toString());
    }

    private void columnasTamanio() {
        for (int i = 1; i <= tabla.getColumnCount() - 1; i++) {
            tabla.getColumnModel().getColumn(i).setPreferredWidth(175);
        }
    }

