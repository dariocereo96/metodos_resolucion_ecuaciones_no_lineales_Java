    private void btnCalcularMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCalcularMouseClicked

        Funcion f = new Funcion();
        MetodoSecante msc = new MetodoSecante();

        if (validarCampos()) {

            msc.setFuncion(this.txtFuncion.getText().trim());
            msc.setAproximaciones(Double.parseDouble(this.aproximacionXo.getText()), Double.parseDouble(this.aproximacionXi.getText()));

            if (!this.txtError.getText().isEmpty()) {
                msc.setError(Double.parseDouble(this.txtError.getText()));
            }

            if (!this.txtIteracion.getText().isEmpty()) {
                msc.setIteraciones(Integer.parseInt(this.txtIteracion.getText()));
            }

            if (!this.txtRaiz.getText().isEmpty()) {
                if (f.evaluar(this.txtFuncion.getText(), Double.parseDouble(this.txtRaiz.getText())) == 0) {
                    msc.setRaiz(Double.parseDouble(this.txtRaiz.getText()));
                } else {
                    JOptionPane.showMessageDialog(this, this.txtRaiz.getText() + " no es raiz exacta", "Raiz Exacta", JOptionPane.ERROR_MESSAGE);
                }
            }

            modelo.setRowCount(0);
            msc.calcular(modelo);
            llenarRespuesta();

        }
    }//GEN-LAST:event_btnCalcularMouseClicked

